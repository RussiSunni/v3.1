using System;
using System.Collections;
using System.Collections.Generic;
using System.IO;
using System.Runtime.Serialization.Formatters.Binary;
using UnityEngine;
using UnityEngine.UI;

public class Game : MonoBehaviour
{



    private Save CreateSaveGameObject()
    {
        Save save = new Save();

        save.nameString = Progress.nameString;
        save.playerCharacter = Progress.playerCharacter;
        save.hello = Progress.hello;
        save.bear = Progress.bear;
        save.monkey = Progress.monkey;
        save.door = Progress.door;
        save.blue = Progress.blue;
        save.green = Progress.green;
        save.red = Progress.red;
        save.yellow = Progress.yellow;
        save.lettersExerciseComplete = Progress.lettersExerciseComplete;

        return save;
    }
    public void SaveGame()
    {
        // 1
        Save save = CreateSaveGameObject();

        // 2
        BinaryFormatter bf = new BinaryFormatter();
        FileStream file = File.Create(Application.persistentDataPath + "/gamesave.save");
        bf.Serialize(file, save);
        file.Close();

        Debug.Log("Game Saved");
    }



    public void LoadGame()
    {
        // 1
        if (File.Exists(Application.persistentDataPath + "/gamesave.save"))
        {

            // 2
            BinaryFormatter bf = new BinaryFormatter();
            FileStream file = File.Open(Application.persistentDataPath + "/gamesave.save", FileMode.Open);
            Save save = (Save)bf.Deserialize(file);
            file.Close();

            Progress.nameString = save.nameString;
            Progress.playerCharacter = save.playerCharacter;
            Progress.hello = save.hello;
            Progress.bear = save.bear;
            Progress.monkey = save.monkey;
            Progress.door = save.door;
            Progress.blue = save.blue;
            Progress.green = save.green;
            Progress.red = save.red;
            Progress.yellow = save.yellow;
            Progress.lettersExerciseComplete = save.lettersExerciseComplete;

            Debug.Log("Game Loaded");

        }
        else
        {
            Debug.Log("No game saved!");
        }
    }

    public void SaveAsJSON()
    {
        Save save = CreateSaveGameObject();
        string json = JsonUtility.ToJson(save);

        Debug.Log("Saving as JSON: " + json);
    }
}
